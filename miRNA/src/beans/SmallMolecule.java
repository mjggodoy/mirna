package beans;

public class SmallMolecule {

	private String FDA;
	private String DB;
	private String CID;
	private String name;

	public SmallMolecule() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFDA() {
		return FDA;
	}

	public void setFDA(String FDA) {
		this.FDA = FDA;
	}

	public String getDB() {
		return DB;
	}

	public void setDB(String DB) {
		this.DB = DB;
	}

	public String getCID() {
		return CID;
	}

	public void setCID(String CID) {
		this.CID = CID;
	}

}