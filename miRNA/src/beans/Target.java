package beans;

public class Target {
	
	private String method;
	private String feature;
	private String chromosome;
	private String end_strand;
	private String start_strand;
	private String location;
	private String phase;
	private String pValue_OG;
	private String targetScore;
	private String miTG_score;
	private String transcriptId;
	private String pubmedId;
	private String year;
	private String name;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Target() {}

	public String getMethod() {
		return method;
	}

	public void setMethod(String method) {
		this.method = method;
	}

	public String getFeature() {
		return feature;
	}

	public void setFeature(String feature) {
		this.feature = feature;
	}

	public String getChromosome() {
		return chromosome;
	}

	public void setChromosome(String chromosome) {
		this.chromosome = chromosome;
	}

	public String getEnd_strand() {
		return end_strand;
	}

	public void setEnd_strand(String end_strand) {
		this.end_strand = end_strand;
	}

	public String getStart_strand() {
		return start_strand;
	}

	public void setStart_strand(String start_strand) {
		this.start_strand = start_strand;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getPhase() {
		return phase;
	}

	public void setPhase(String phase) {
		this.phase = phase;
	}

	public String getpValue_OG() {
		return pValue_OG;
	}

	public void setpValue_OG(String pValue_OG) {
		this.pValue_OG = pValue_OG;
	}

	public String getTargetScore() {
		return targetScore;
	}

	public void setTargetScore(String targetScore) {
		this.targetScore = targetScore;
	}

	public String getMiTG_score() {
		return miTG_score;
	}

	public void setMiTG_score(String miTG_score) {
		this.miTG_score = miTG_score;
	}

	public String getTranscriptId() {
		return transcriptId;
	}

	public void setTranscriptId(String transcriptId) {
		this.transcriptId = transcriptId;
	}

	public String getPubmedId() {
		return pubmedId;
	}

	public void setPubmedId(String pubmedId) {
		this.pubmedId = pubmedId;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	
	
}
